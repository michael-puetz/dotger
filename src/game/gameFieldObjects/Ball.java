package game.gameFieldObjects;

import game.shapes.Circle;

import java.awt.Color;

public abstract class Ball extends GameFieldObject {

	public Ball(double posX, double posY, double radius, Color color) {
		super(posX, posY, new Circle(radius), color);
	}

	public double getRadius() {
		return ((Circle)shape).getRadius();
	}

}
